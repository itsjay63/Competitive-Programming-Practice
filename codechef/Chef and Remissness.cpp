#include<bits/stdc++.h>
using namespace std;

#define ll long long

int main()
{
    ll t;
    cin>>t;
    while(t--)
    {
        ll a,b;
        cin>>a>>b;
        if(a>=b)
        {
            cout<<a<<" ";
        }
        else
        {
            cout<<b<<" ";
        }
        cout<<a+b;
    }
    return 0;
}


