t = []
p = []
h = []
for i in range(1, 100000):
    a = ((i * (i + 1)) // 2)
    t.append(a)
    b = ((i * (3 * i - 1)) // 2)
    p.append(b)
    c = (i * (2 * i - 1))
    h.append(c)

for j in range(0, 99999):
    if (t[j] in p) and (t[j] in h):
        print(t[j])
        print(j+1)
