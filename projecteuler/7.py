import math
count=0
a = []
for i in range(104750):
    a.append(i)
a[0]=0
a[1]=0
k = int(math.sqrt(104750))
for i in range(2,k + 1):
    for j in range(2,104750):
        n = i * j
        if n < 104750:
            a[n] = 0
for i in range(104750):
    if a[i]!=0:
        v=i
        count+=1

print(count,v)
print(a[v])