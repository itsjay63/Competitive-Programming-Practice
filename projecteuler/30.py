import math

def isnum(n):
    sum=0
    while n>0:
        j=n%10
        j=int(math.pow(j,5))
        sum=sum+j
        n=n//10

    return sum

m=0
for i in range(2,1000000):
    k=isnum(i)
    if(i==k):
        print(k)
        m=m+k

print(m)