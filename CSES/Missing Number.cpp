#include<bits/stdc++.h>
using namespace std;
int main()
{
    int n,sum=0,sum1=0;
    cin>>n;
    for(int i=1;i<=n;i++)
    {
        sum = sum +i;
    }
    int m;
    for(int i=1;i<=n-1;i++)
    {
        cin>>m;
        sum1 = sum1 +m;
    }
    cout<<sum-sum1<<endl;

    return 0;
}
